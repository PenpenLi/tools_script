bool IsSameDay(unsigned long long uTime1, unsigned long long uTime2)
{
	unsigned int uDay1 = uTime1 / DAY_SECOND;
	unsigned int uDay2 = uTime2 / DAY_SECOND;
	return (uDay1 == uDay2);

}